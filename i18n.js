let currentLang = localStorage.getItem('tm_lang') || 'uz';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('tm_lang', lang);
  
  // Update UI Elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18n[lang][key]) {
      if (key === 'heroTitle') { // Special case for innerHTML
        el.innerHTML = i18n[lang][key];
      } else {
        el.textContent = i18n[lang][key]; 
      }
    }
  });

  // Update input placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (i18n[lang][key]) {
      el.placeholder = i18n[lang][key];
    }
  });

  // Update buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

// Export for module usage or global access
if (typeof window !== 'undefined') window.setLang = setLang;
